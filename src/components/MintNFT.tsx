const MintNFT = ({ provider, address, userId }: { provider: ethers.BrowserProvider | null, address: string | null, userId: string | null; }) => {
    const [txStatus, setTxStatus] = useState<string>("");
    const [txHash, setTxHash] = useState<string>("");
    const [loading, setLoading] = useState(false);
    const [selectedFile, setSelectedFile] = useState<File | null>(null);

    const handleFileChange = (e: ChangeEvent<HTMLInputElement>) => {
        if (e.target.files?.length) {
            const file = e.target.files[0];
            setSelectedFile(new File([file], file.name, { type: file.type }));
        }
    };

    const handleMint = async () => {
        if (!provider || !address) {
            setTxStatus("Please connect your wallet.");
            return;
        }
        if (!userId) {
            setTxStatus("Please verify with Civic first.");
            return;
        }
        if (!selectedFile) {
            setTxStatus("Please select an image to upload.");
            return;
        }
        setLoading(true);
        setTxStatus("");
        try {
            const civicHash = keccak256(toUtf8Bytes(userId));
            const signer = await provider.getSigner();
            const contract = new Contract(CONTRACT_ADDRESS, CONTRACT_ABI, signer);
            const imageUrl = await uploadImageToPinata(selectedFile);

            const metadataURI = await uploadToPinata({
                name: "Proof of Personhood NFT",
                description: "This NFT proves you are a unique, verified human via Civic.",
                image: imageUrl, 
            });
            console.log("Stored NFT metadata at:", metadataURI);

            const tx = await contract.mint(civicHash, metadataURI);
            setTxStatus("Transaction sent. Waiting for confirmation...");
            setTxHash(tx.hash);
            
            await tx.wait();
            setTxStatus("NFT minted successfully!");
        } catch (err: any) {
            setTxStatus(err.message || "Minting failed.");
        }
        setLoading(false);
    };

    return (
        <div className="my-8 p-6 bg-white rounded-xl shadow flex flex-col items-center">
            <h3 className="text-xl font-bold mb-2">Mint your Proof of Personhood NFT</h3>
            <input
                type="file"
                accept="image/*"
                onChange={handleFileChange}
                className="mb-4"
            />
            <button
                onClick={handleMint}
                disabled={loading}
                className="px-6 py-2 bg-green-600 text-white rounded-lg shadow hover:bg-green-700 transition disabled:opacity-50"
            >
                {loading ? "Minting..." : "Mint NFT"}
            </button>
            {txStatus && <div className="mt-4 text-blue-700">{txStatus}</div>}
            {txHash && (
                <a
                    href={`https://goerli.etherscan.io/tx/${txHash}`}
                    target="_blank"
                    rel="noopener noreferrer"
                    className="mt-2 text-blue-500 underline"
                >
                    View on Etherscan
                </a>
            )}
            
        </div>
    );
};

export default MintNFT;
